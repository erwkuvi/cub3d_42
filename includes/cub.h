typedef struct s_img{
    unsigned int    *img;
    int             *addr;
    int             bpp;
    int             line_length;
    int             height;
    int             width;
    int             endian;
    bool            is_loaded;
}               t_img;

typedef struct s_map{
    char            **map;
    int             y;
    int             x;
    char            *no_tex;
    char            *so_tex;
    char            *we_tex;
    char            *ea_tex;
    int             floor_color;
    int             ceiling_color;
}               t_map;

typedef struct s_data{
    t_map           *map;
    t_img           *img;
}               t_data;

/*------error.c-------*/
int     ft_error(char *message, int ret_val, t_data *data);
void    ft_free_data(t_data *data);

/*------readfile.c-------*/
int     open_map(char *filename);
void    read_map(int *fd, t_map *map);

#endif
